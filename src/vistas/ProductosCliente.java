    private void CargarProductos() {
        limpiarTabla();

        tablaProductosCliente.setDefaultRenderer(Object.class, new RenderImagen());

        ArrayList ProdutosLista;
        Productos mProducto;

        if (con.ConectarBD()) {
            Object Datos[] = new Object[5];
            ProdutosLista = con.CargarProductos();

            if (ProdutosLista != null) {
                for (int i = 0; i < ProdutosLista.size(); i++) {
                    mProducto = (Productos) ProdutosLista.get(i);
                    Datos[0] = String.valueOf(mProducto.getId_producto());
                    Datos[1] = mProducto.getNombre_producto();
                    Datos[2] = String.valueOf(mProducto.getPrecio_producto());
                    Datos[3] = String.valueOf(mProducto.getStock_producto());

                    try {
                        byte[] imagen = mProducto.getImagen_producto();
                        BufferedImage bufferedImage = null;
                        InputStream inputStream = new ByteArrayInputStream(imagen);
                        bufferedImage = ImageIO.read(inputStream);
                        ImageIcon mIcono = new ImageIcon(bufferedImage.getScaledInstance(80, 80, 80));
                        Datos[4] = new JLabel(mIcono);
                    } catch (Exception e) {
                        Datos[4] = new JLabel("No Imagen");
                    }

                    modeloTabla.addRow(Datos);
                }
                tablaProductosCliente.setModel(modeloTabla);
                tablaProductosCliente.setRowHeight(100);
                tablaProductosCliente.getColumnModel().getColumn(0).setPreferredWidth(60);
                tablaProductosCliente.getColumnModel().getColumn(1).setPreferredWidth(60);
                tablaProductosCliente.getColumnModel().getColumn(2).setPreferredWidth(60);
                tablaProductosCliente.getColumnModel().getColumn(3).setPreferredWidth(60);
                tablaProductosCliente.getColumnModel().getColumn(4).setPreferredWidth(100);

            }
        }
    }
    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        try {
        int id = Integer.parseInt(txtID.getText());
        String nombre = txtNombre.getText();
        double precio = Double.parseDouble(txtPrecio.getText());
        int cantidad = Integer.parseInt(txtCantidad.getText());
        double subtotal = Double.parseDouble(txtCantidad1.getText());

        byte[] imagen = null;
        Icon icono = lblImagen.getIcon();
        if (icono != null && icono instanceof ImageIcon) {
            BufferedImage bufferedImage = new BufferedImage(icono.getIconWidth(), icono.getIconHeight(), BufferedImage.TYPE_INT_RGB);
            Graphics g = bufferedImage.createGraphics();
            icono.paintIcon(null, g, 0,0);
            g.dispose();
            ByteArrayOutputStream byteArrayOutputStream = new ByteArrayOutputStream();
            ImageIO.write(bufferedImage, "jpg", byteArrayOutputStream);
            imagen = byteArrayOutputStream.toByteArray();
        }

        con.AgregarProductoPedido(id, nombre, precio, imagen, cantidad, subtotal);

        JOptionPane.showMessageDialog(this, "Producto agregado al pedido correctamente");
    } catch (NumberFormatException | IOException e) {
        JOptionPane.showMessageDialog(this, "Error al procesar la informaci√≥n.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_BtnAgregarActionPerformed

    private void limpiarTabla() {
        int rowCount = modeloTabla.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            modeloTabla.removeRow(i);
        }
    }

    void limpiar() {
        txtID.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
    }

    private void tablaProductosMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = tablaProductosCliente.rowAtPoint(evt.getPoint());
        if (fila >= 0) {
            txtID.setText(tablaProductosCliente.getValueAt(fila, 0).toString());
            txtNombre.setText(tablaProductosCliente.getValueAt(fila, 1).toString());
            txtPrecio.setText(tablaProductosCliente.getValueAt(fila, 2).toString());
            txtStock.setText(tablaProductosCliente.getValueAt(fila, 3).toString());

            Object valorCelda = tablaProductosCliente.getValueAt(fila, 4);
            if (valorCelda instanceof JLabel) {
                JLabel labelImagen = (JLabel) valorCelda;
                Icon icono = labelImagen.getIcon();
                if (icono != null) {
                    ImageIcon imageIcon = (ImageIcon) icono;
                    Image image = imageIcon.getImage();

                    Image scaledImage = image.getScaledInstance(lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_SMOOTH);
                    ImageIcon scaledIcon = new ImageIcon(scaledImage);

                    lblImagen.setIcon(scaledIcon);
                } else {
                    lblImagen.setIcon(null);
                }
            } else {
                lblImagen.setIcon(null);
            }
        }
    }

